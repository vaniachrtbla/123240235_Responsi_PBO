    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // validasi input buku
        String jenis = cbJenis.getSelectedItem().toString();
        String txtJudul = judul.getText();
        String txtTahun = tahun.getText();
        String txtPengarang = pengarang.getText();
        String txtISBN = ISBN.getText();
        String txtEdisi = edisi.getText();
        String txtPenerbit = penerbit.getText();
        
        if (jenis.equals("Buku")) {
            if (
                txtJudul.isEmpty()
                || txtTahun.isEmpty()
                || txtPengarang.isEmpty()
                || txtISBN.isEmpty()
            ) {
                JOptionPane.showMessageDialog(this, "Data Buku harus lengkap!");
                return;
            }
        }

        // validasi input majalah
        if (jenis.equals("Majalah")) {
            if (
                txtJudul.isEmpty()
                || txtTahun.isEmpty()
                || txtEdisi.isEmpty()
                || txtPenerbit.isEmpty()
            ) {
                JOptionPane.showMessageDialog(
                        this, "Data Majalah harus lengkap!");
                return;
            }
        }

        // validasi tahun harus angka
        int tahunTerbit;
        try {
            tahunTerbit = Integer.parseInt(txtTahun);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(
                    this, "Tahun harus berupa angka!");
            return;
        }

        // proses edit data
        if (modeEdit) {
            controller.editItem(
                    idItem,
                    jenis,
                    txtJudul,
                    tahunTerbit,
                    txtPengarang,
                    txtISBN,
                    txtEdisi,
                    txtPenerbit
            );
            JOptionPane.showMessageDialog(this,
                    "Data berhasil diupdate!");
        
        // proses tambah data
        } else {    
            controller.tambahItem(
                    jenis,
                    txtJudul,
                    tahunTerbit,
                    txtPengarang,
                    txtISBN,
                    txtEdisi,
                    txtPenerbit
            );
            JOptionPane.showMessageDialog(this, "Data berhasil ditambah!");
        }        
        dispose();
    }//GEN-LAST:event_btnSimpanActionPerformed

