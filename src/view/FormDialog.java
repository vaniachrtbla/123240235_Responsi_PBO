    private void cbJenisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbJenisActionPerformed
        String jenis = cbJenis.getSelectedItem().toString();
        
        // field buku aktif
        if (jenis.equals("Buku")) {

            pengarang.setEnabled(true);
            ISBN.setEnabled(true);

            edisi.setEnabled(false);
            penerbit.setEnabled(false);

            // kosongkan field majalah
            edisi.setText("");
            penerbit.setText("");
        }

        // field majalah aktif
        else if (jenis.equals("Majalah")) {

            pengarang.setEnabled(false);
            ISBN.setEnabled(false);

            edisi.setEnabled(true);
            penerbit.setEnabled(true);

            // kosongkan field buku
            pengarang.setText("");
            ISBN.setText("");
        }
    }//GEN-LAST:event_cbJenisActionPerformed

